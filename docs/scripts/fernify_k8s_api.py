OPERATOR_SRC = "https://github.com/ai-dynamo/dynamo/tree/main/deploy/operator"
OPERATOR_DIR = REPO_ROOT / "deploy" / "operator"

_RESOURCE_LINK_RE = re.compile(r"^-\s+\[(\w+)\]")
_TYPE_HEADING_RE = re.compile(r"^####\s+(.+?)\s*$", re.MULTILINE)


# ---------------------------------------------------------------------------
# Source discovery
# ---------------------------------------------------------------------------

_GO_SOURCE_INDEX: dict[str, Path] | None = None


def _get_go_source_index() -> dict[str, Path]:
    """Lazily build a filename -> Path index of Go type files."""
    global _GO_SOURCE_INDEX  # noqa: PLW0603
    if _GO_SOURCE_INDEX is None:
        _GO_SOURCE_INDEX = {}
        if OPERATOR_DIR.exists():
            for f in OPERATOR_DIR.glob("api/*/*.go"):
                _GO_SOURCE_INDEX[f.name] = f
    return _GO_SOURCE_INDEX


def _discover_go_source(resource_name: str) -> str:
    """Find the Go source file for a resource type by naming convention."""
    index = _get_go_source_index()
    name_lower = resource_name.lower()
    base = name_lower.removeprefix("dynamo")
    for pattern in (
        f"{name_lower}_types.go",
        f"dynamo_{base}_types.go",
        f"{base}_types.go",
    ):
        if pattern in index:
            return str(index[pattern].relative_to(OPERATOR_DIR))
    return ""


def _short_name(name: str) -> str:
    """Drop 'Dynamo' prefix and split CamelCase for readable card titles."""
    name = name.removeprefix("Dynamo")
    return re.sub(r"(?<!^)(?=[A-Z])", " ", name)


def _first_content_line(text: str) -> str:
    """Extract the first non-empty, non-metadata line from a text block."""
    for line in text.split("\n"):
        stripped = line.strip()
        if stripped and not stripped.startswith("_") and not stripped.startswith("|"):
            return stripped[:77] + "..." if len(stripped) > 80 else stripped
    return ""


def _extract_description(text: str, resource_name: str) -> str:
    """Extract the first content line after a #### resource heading."""
    pattern = re.compile(
        rf"^####\s+{re.escape(resource_name)}\s*$\n(.*?)(?=^####\s|\Z)",
        re.MULTILINE | re.DOTALL,
    )
    m = pattern.search(text)
    if m:
        return _first_content_line(m.group(1))
    return ""


def _resolve_meta(name: str, full_text: str) -> dict[str, str]:
    """Resolve icon, description, and source path for a resource."""
    if name in RESOURCE_META:
        return RESOURCE_META[name]
    desc = (
        _extract_description(full_text, name)
        or "Custom resource for the Dynamo operator."
    )
    return {"icon": "regular cube", "desc": desc, "src": _discover_go_source(name)}


# ---------------------------------------------------------------------------
# Pipeline stage 1: build_resource_cards (text -> text)
# ---------------------------------------------------------------------------

_RESOURCE_SECTION_RE = re.compile(
    r"^### Resource Types\s*\n((?:-\s+\[.+?\]\n?)+)",
    re.MULTILINE,
)


def build_resource_cards(text: str, meta: dict[str, dict[str, str]]) -> str:
    """Replace ``### Resource Types`` bullet lists with Fern CardGroups."""

    def _replace(m: re.Match) -> str:
        lines = m.group(1).strip().split("\n")
        resources: list[str] = []
        for line in lines:
            rm = _RESOURCE_LINK_RE.match(line.strip())
            if rm:
                resources.append(rm.group(1))
        if not resources:
            return m.group(0)

        cards: list[dict[str, str]] = []
        for name in resources:
            info = _resolve_meta(name, text)
            src = info["src"]
            href = f"{OPERATOR_SRC}/{src}" if src else ""
            cards.append(
                {
                    "title": _short_name(name),
                    "icon": info["icon"],
                    "description": info["desc"],
                    "href": href,
                }
            )
        return render_card_group(cards, min(len(cards), 3))

    return _RESOURCE_SECTION_RE.sub(_replace, text)


# ---------------------------------------------------------------------------
# Pipeline stage 3: wrap_api_groups_in_tabs (text -> text)
# ---------------------------------------------------------------------------

_API_GROUP_RE = re.compile(
    r"^## (nvidia\.com/v1alpha1|nvidia\.com/v1beta1|operator\.config\.dynamo\.nvidia\.com/v1alpha1)\s*$",
    re.MULTILINE,
)

_TAB_TITLES: dict[str, str] = {
    "nvidia.com/v1alpha1": "v1alpha1",
    "nvidia.com/v1beta1": "v1beta1",
    "operator.config.dynamo.nvidia.com/v1alpha1": "Operator Config",
}


def wrap_api_groups_in_tabs(text: str) -> str:
    """Wrap ## API group sections in Fern <Tabs>/<Tab> components."""
    matches = list(_API_GROUP_RE.finditer(text))
    if not matches:
        return text

    before = text[: matches[0].start()]

    footer_marker = "\n# Operator Default Values Injection"
    footer_idx = text.find(footer_marker)
    after_tabs = ""
    end_of_tabs = len(text)

    if footer_idx >= 0:
        end_of_tabs = footer_idx
        footer_text = text[footer_idx:].lstrip("\n")
        if footer_text.startswith("# "):
            nl = footer_text.find("\n")
            if nl > 0:
                title = footer_text[2:nl].strip()
                body = footer_text[nl + 1 :].strip()
            else:
                title = "Operator Default Values"
                body = footer_text[nl + 1 :].strip() if nl > 0 else ""
        else:
            title = "Operator Default Values"
            body = footer_text
        after_tabs = "\n" + render_accordion(title, body)

    tabs_parts: list[str] = ["<Tabs>\n\n"]
    for i, match in enumerate(matches):
        group_name = match.group(1)
        tab_title = _TAB_TITLES.get(group_name, group_name)
        section_start = match.end()
        section_end = matches[i + 1].start() if i + 1 < len(matches) else end_of_tabs
        section_body = text[section_start:section_end].strip()

        tabs_parts.append(f'<Tab title="{tab_title}">\n\n')
        tabs_parts.append(section_body)
        tabs_parts.append("\n\n</Tab>\n\n")

    tabs_parts.append("</Tabs>\n")

    return before + "".join(tabs_parts) + after_tabs


# ---------------------------------------------------------------------------
# Idempotency guard
# ---------------------------------------------------------------------------


def _is_already_fernified(text: str) -> bool:
    """Detect if the file has already been processed (contains Fern MDX)."""
    for line in text.split("\n")[:50]:
        if "<Tabs>" in line or "<CardGroup" in line or "<Accordion" in line:
            return True
    return False


# ---------------------------------------------------------------------------
# Main pipeline
# ---------------------------------------------------------------------------


def fernify(text: str) -> str:
    """Apply all Fern transforms to the K8s API reference."""
    text = build_resource_cards(text, RESOURCE_META)
    text = fernify_headings_to_accordion(text, 4)
    text = wrap_api_groups_in_tabs(text)
    text = convert_admonitions(text)
    return text


def main() -> None:
    """Entry point: parse args and fernify the K8s API reference."""
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "input",
        nargs="?",
        type=Path,
        default=DEFAULT_INPUT,
        help="Path to api-reference.md (default: docs/kubernetes/api-reference.md)",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=None,
        help="Output path (default: overwrite input file)",
    )
    args = parser.parse_args()

    if not args.input.exists():
        sys.exit(f"Input file not found: {args.input}")

    print(f"Fernifying {args.input}...")
    text = args.input.read_text()

    if _is_already_fernified(text):
        print("  WARNING: File already contains Fern components. Skipping.")
        print(
            "  To re-fernify, first restore the raw version: git checkout HEAD -- <file>"
        )
        content = text
    else:
        content = fernify(text)

    out_path = args.output or args.input
    out_path.write_text(content)
    print(f"  -> {out_path} ({content.count(chr(10))} lines)")


if __name__ == "__main__":
    main()
