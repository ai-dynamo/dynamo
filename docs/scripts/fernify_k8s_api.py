"""Transform the K8s CRD API reference Markdown into Fern MDX.

Reads docs/kubernetes/api-reference.md (generated by crd-ref-docs) and
applies a pipeline of composable text transforms:

    build_resource_cards  ->  fernify_headings_to_accordion
        ->  wrap_api_groups_in_tabs  ->  convert_admonitions

This script does NOT modify content -- only wraps it in Fern MDX
components for better navigation. It is safe to re-run after each
``make generate-api-docs`` in deploy/operator/.

Usage (from repository root):
    python3 docs/scripts/fernify_k8s_api.py
"""

