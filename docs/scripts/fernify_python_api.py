"""Transform the Python API reference Markdown into Fern MDX.

Reads docs/api/python/README.md (generated by generate_python_api.py)
and applies composable text transforms to convert GitHub-friendly
constructs into Fern components.

Usage (from repository root):
    python3 docs/scripts/fernify_python_api.py
"""

from __future__ import annotations

import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))
from _fern_helpers import (  # noqa: E402
    REPO_ROOT,
    fernify_details_to_accordion,
    fernify_table_to_cards,
)

sys.path.insert(0, str(REPO_ROOT / "fern"))
from convert_callouts import convert_admonitions  # noqa: E402

DEFAULT_INPUT = REPO_ROOT / "docs" / "api" / "python" / "README.md"

MODULE_ICONS: dict[str, str] = {
    "Core Bindings": "regular microchip",
    "dynamo.runtime": "regular play",
    "dynamo.llm": "regular brain",
    "dynamo.frontend": "regular globe",
    "dynamo.common": "regular toolbox",
    "dynamo.health_check": "regular heart-pulse",
    "dynamo.logits_processing": "regular sliders",
    "dynamo.planner": "regular chart-line",
    "dynamo.router": "regular route",
    "dynamo.mocker": "regular vial",
    "dynamo.nixl_connect": "regular network-wired",
}


def fernify(text: str) -> str:
    """Apply all Fern transforms to the Python API page."""
    text = fernify_table_to_cards(text, MODULE_ICONS)
    text = fernify_details_to_accordion(text)
    text = convert_admonitions(text)
    return text


def main() -> None:
    """Entry point: fernify the Python API reference in-place."""
    input_path = DEFAULT_INPUT
    if not input_path.exists():
        sys.exit(f"Input file not found: {input_path}")

    print(f"Fernifying {input_path}...")
    content = fernify(input_path.read_text())
    input_path.write_text(content)
    print(f"  -> {input_path} ({content.count(chr(10))} lines)")


if __name__ == "__main__":
    main()
