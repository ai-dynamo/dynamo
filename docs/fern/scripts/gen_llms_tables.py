# page -> (renderer, wrap_in_llms_only). The three component-backed pages get
# <llms-only> twins (humans see the React components); releases-data.mdx IS
# the page body, human-viewable and machine-consumable alike.
PAGES = {
    "compatibility.mdx": (render_compatibility, True),
    "release-artifacts.mdx": (render_release_artifacts, True),
    "model-early-access-builds.mdx": (render_model_ea_builds, True),
    "releases-data.mdx": (render_releases_data, False),
}

# Standalone machine-readable outputs (path -> builder returning full text).
ASSET_OUTPUTS = {
    JSON_PATH: build_json,
    ATOM_PATH: build_atom,
}

# Matches an existing generated span. Tolerant of edits to the note after
# "llms-tables:begin" so a hand-tweaked marker comment is still replaced.
BLOCK_RE = re.compile(
    r"\{/\*\s*llms-tables:begin[^*]*\*/\}.*?\{/\*\s*llms-tables:end\s*\*/\}",
    re.DOTALL,
)


def build_block(body: str, wrap: bool = True) -> str:
    if wrap:
        return f"{MARKER_BEGIN}\n<llms-only>\n\n{body}\n\n</llms-only>\n{MARKER_END}"
    return f"{MARKER_BEGIN}\n\n{body}\n\n{MARKER_END}"


def apply_block(page_text: str, block: str) -> str:
    if BLOCK_RE.search(page_text):
        return BLOCK_RE.sub(lambda _m: block, page_text, count=1)
    # No markers yet: append at end of file, before the trailing newline.
    return page_text.rstrip("\n") + "\n\n" + block + "\n"


def main(argv: list[str]) -> int:
    ap = argparse.ArgumentParser(description=__doc__.splitlines()[0])
    ap.add_argument(
        "--check",
        action="store_true",
        help="exit 1 if regeneration would change any page; write nothing",
    )
    args = ap.parse_args(argv)

    try:
        data = parse_data_module(DATA_TS)
    except TSParseError as exc:
        print(f"error: failed to parse {DATA_TS}: {exc}", file=sys.stderr)
        return 2

    # Render everything up front so a failure on any output emits nothing.
    try:
        rendered: dict[Path, str] = {}
        for page_name, (renderer, wrap) in PAGES.items():
            page_path = REFERENCE_DIR / page_name
            if not page_path.is_file():
                print(f"error: page not found: {page_path}", file=sys.stderr)
                return 2
            old = page_path.read_text(encoding="utf-8")
            rendered[page_path] = apply_block(old, build_block(renderer(data), wrap))
        for asset_path, builder in ASSET_OUTPUTS.items():
            rendered[asset_path] = builder(data)
    except (TSParseError, KeyError) as exc:
        print(f"error: rendering failed: {exc!r}", file=sys.stderr)
        return 2

    stale = []
    for path, new_text in rendered.items():
        name = path.name
        old_text = path.read_text(encoding="utf-8") if path.is_file() else None
        if new_text == old_text:
            print(f"{name}: unchanged")
            continue
        stale.append(name)
        if args.check:
            print(f"{name}: STALE (regeneration would change it)")
        else:
            path.write_text(new_text, encoding="utf-8")
            print(f"{name}: wrote {len(new_text.encode('utf-8'))} bytes")

    if args.check and stale:
        print(
            f"check failed: {len(stale)} output(s) stale — run gen_llms_tables.py",
            file=sys.stderr,
        )
        return 1
    return 0


if __name__ == "__main__":
    sys.exit(main(sys.argv[1:]))
