_MONTHS = {
    "Jan": 1, "Feb": 2, "Mar": 3, "Apr": 4, "May": 5, "Jun": 6,
    "Jul": 7, "Aug": 8, "Sep": 9, "Oct": 10, "Nov": 11, "Dec": 12,
}  # fmt: skip

_DATE_RE = re.compile(r"^([A-Z][a-z]{2}) (\d{1,2}), (\d{4})$")


def iso_date(human: str) -> str:
    """'Jul 20, 2026' -> '2026-07-20'. Fails closed on anything else."""
    m = _DATE_RE.match(human.strip())
    if not m or m.group(1) not in _MONTHS:
        raise TSParseError(f"unparseable date {human!r} (expected 'Mon D, YYYY')")
    return f"{int(m.group(3)):04d}-{_MONTHS[m.group(1)]:02d}-{int(m.group(2)):02d}"


def release_link(rel: dict) -> str | None:
    """Docs-native notes URL on the canonical prod host; GitHub fallback."""
    href = rel.get("notesHref")
    if href:
        return PROD_BASE + href.removeprefix("/dynamo")
    return rel.get("github")


