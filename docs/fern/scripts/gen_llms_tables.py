def build_json(data: dict) -> str:
    """Stable-schema JSON serialization of the parsed releases.data.ts."""
    releases = []
    for rel in data["RELEASES"]:
        out = dict(rel)
        if rel.get("date"):
            out["dateIso"] = iso_date(rel["date"])
        link = release_link(rel)
        if link:
            out["notesUrl"] = link
        releases.append(out)

    ea_builds = []
    for b in data["MODEL_EA_BUILDS"]:
        out = dict(b)
        out["shippedIso"] = iso_date(b["shipped"])
        ea_builds.append(out)

    if not any("dateIso" in r for r in releases):
        raise TSParseError("no dated RELEASES entries — cannot build releases.json")

    payload = {
        "source": "docs/fern/components/releases.data.ts",
        "generator": "docs/fern/scripts/gen_llms_tables.py",
        "updated": max(r["dateIso"] for r in releases if "dateIso" in r),
        "current": {
            "version": data["CURRENT_VERSION"],
            "date": data["CURRENT_DATE"],
            "dateIso": iso_date(data["CURRENT_DATE"]),
            "tag": data["CURRENT_TAG"],
            "wheel": data["CURRENT_WHEEL"],
        },
        "mainTot": data["MAIN_TOT"],
        "releases": releases,
        "cudaHistory": data["CUDA_HISTORY"],
        "cudaNotes": data.get("CUDA_NOTES") or [],
        "features": data["FEATURES"],
        "artifacts": data["ARTIFACTS"],
        "modelEaBuilds": ea_builds,
        "platformPreviewCoverage": data["PLATFORM_PREVIEW_COVERAGE"],
        "platform": data["PLATFORM"],
        "knownArtifactIssues": data["KNOWN_ARTIFACT_ISSUES"],
        "cratesFirstPublished": data["CRATES_FIRST_PUBLISHED"],
        "releaseStats": data["RELEASE_STATS"],
        "nightliesNote": data.get("NIGHTLIES_NOTE"),
    }
    return json.dumps(payload, indent=2, ensure_ascii=False) + "\n"


