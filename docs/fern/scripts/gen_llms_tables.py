def xml_escape(text: str) -> str:
    return (
        text.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace('"', "&quot;")
    )


def build_atom(data: dict) -> str:
    """Atom 1.0 feed of RELEASES, newest first, deterministic timestamps."""
    dated = []
    for index, rel in enumerate(data["RELEASES"]):
        if not rel.get("date"):
            continue  # undated entries cannot be ordered in a feed
        dated.append((iso_date(rel["date"]), index, rel))
    if not dated:
        raise TSParseError("no dated RELEASES entries — cannot build the Atom feed")
    # Newest first; ties keep releases.data.ts order (already newest-first).
    dated.sort(key=lambda item: (item[0], -item[1]), reverse=True)

    feed_updated = dated[0][0] + "T00:00:00Z"
    notes_index = f"{PROD_BASE}/dev/reference/releases/release-history"

    lines = [
        '<?xml version="1.0" encoding="utf-8"?>',
        "<!--",
        "SPDX-FileCopyrightText: Copyright (c) 2025-2026 NVIDIA CORPORATION"
        " & AFFILIATES. All rights reserved.",
        "SPDX-License-Identifier: Apache-2.0",
        "",
        "Generated by docs/fern/scripts/gen_llms_tables.py from"
        " docs/fern/components/releases.data.ts - do not edit.",
        "-->",
        '<feed xmlns="http://www.w3.org/2005/Atom">',
        "  <title>NVIDIA Dynamo releases</title>",
        f"  <id>{xml_escape(notes_index)}</id>",
        f'  <link rel="alternate" href="{xml_escape(notes_index)}"/>',
        f"  <updated>{feed_updated}</updated>",
        "  <author><name>NVIDIA Dynamo</name></author>",
    ]
    for date, _index, rel in dated:
        link = release_link(rel)
        entry_id = rel.get("github") or link
        if not entry_id or not link:
            raise TSParseError(
                f"release {rel['version']}: no notesHref or github link for the feed"
            )
        summary = rel.get("notesSummary") or rel.get("delta") or rel.get("note")
        lines += [
            "  <entry>",
            f"    <title>Dynamo {xml_escape(rel['version'])} ({xml_escape(rel['kind'])})</title>",
            f"    <id>{xml_escape(entry_id)}</id>",
            f'    <link rel="alternate" href="{xml_escape(link)}"/>',
            f"    <updated>{date}T00:00:00Z</updated>",
        ]
        if summary:
            lines.append(f"    <summary>{xml_escape(summary)}</summary>")
        lines.append("  </entry>")
    lines.append("</feed>")
    return "\n".join(lines) + "\n"


