def cell(value) -> str:
    """Sanitize a value for a markdown table cell."""
    if value is None or value == "":
        return "-"
    return str(value).replace("|", "\\|").replace("\n", " ")


def md_table(headers: list[str], rows: list[list]) -> str:
    lines = [
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]
    for row in rows:
        lines.append("| " + " | ".join(cell(v) for v in row) + " |")
    return "\n".join(lines)


def nixl_cell(pins: dict) -> str:
    parts = [pins.get(k) or "-" for k in ("nixlSglang", "nixlTrtllm", "nixlVllm")]
    return " / ".join(parts) if any(p != "-" for p in parts) else "-"


FEATURE_STATUS_LABEL = {
    "yes": "Supported",
    "caveat": "Supported with caveat",
    "wip": "Experimental",
    "no": "Not supported",
}


def feature_cell(fc: dict) -> str:
    status = fc.get("status")
    if status not in FEATURE_STATUS_LABEL:
        raise TSParseError(f"unknown feature status {status!r}")
    label = FEATURE_STATUS_LABEL[status]
    note = fc.get("note")
    return f"{label} ({note})" if note else label


