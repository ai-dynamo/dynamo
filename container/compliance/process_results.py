"""Convert SBOM/TSV extraction output to attribution CSV and ATTRIBUTIONS-*.md files.

Reads dpkg.tsv and python.tsv from a target extraction directory (the CSV pipeline
preserves the prior byte-identical behavior) and optionally reads sbom.spdx.json,
lockfiles, and a native_packages.yaml overlay to emit per-ecosystem
ATTRIBUTIONS-*.md files.

Usage:
    python process_results.py --target-dir <dir> --output attribution.csv
    python process_results.py --target-dir <dir> --base-dir <dir> --output attribution.csv
    python process_results.py --target-dir <dir> --cargo-lock Cargo.lock --go-mod go.mod
"""

import argparse
import csv
import functools
import importlib.util
import json
import sys
from pathlib import Path
from typing import Any

# Python stdlib does not ship YAML; compliance tooling already depends on PyYAML
# (it is used elsewhere in container/ CI). Fail loudly if missing so the operator
# knows to install it.
try:
    import yaml
except ImportError:  # pragma: no cover - only triggered when PyYAML not installed
    yaml = None  # type: ignore[assignment]


# ---- Markdown header template -------------------------------------------------

_MD_HEADER = (
    "<!--\n"
    "SPDX-FileCopyrightText: Copyright (c) 2024-2026 NVIDIA CORPORATION & AFFILIATES. All rights reserved.\n"
    "SPDX-License-Identifier: Apache-2.0\n"
    "-->\n"
    "\n"
    "# Third-Party Software Attributions — {ecosystem}\n"
    "\n"
    "This file is automatically generated by `container/compliance/process_results.py`. Do not edit it directly.\n"
)


# ---- Existing CSV pipeline (preserved byte-for-byte) --------------------------


def read_tsv(tsv_path: Path, pkg_type: str) -> list[dict[str, str]]:
    """Parse a tab-separated extraction output file into package dicts."""
    packages = []
    if not tsv_path.is_file():
        return packages
    for line in tsv_path.read_text(errors="replace").strip().splitlines():
        parts = line.split("\t", 2)
        if len(parts) != 3:
            continue
        pkg_name, version, spdx_license = parts
        packages.append(
            {
                "package_name": pkg_name,
                "version": version,
                "type": pkg_type,
                "spdx_license": spdx_license,
            }
        )
    return packages


def read_extraction_dir(directory: Path) -> list[dict[str, str]]:
    """Read dpkg.tsv and python.tsv from an extraction directory."""
    packages = read_tsv(directory / "dpkg.tsv", "dpkg")
    packages += read_tsv(directory / "python.tsv", "python")
    return packages


def compute_diff(
    target_packages: list[dict[str, str]],
    base_packages: list[dict[str, str]],
) -> list[dict[str, str]]:
    """Return packages in target that are new or have a different version vs base."""
    base_lookup = {
        (pkg["package_name"], pkg["type"]): pkg["version"] for pkg in base_packages
    }
    return [
        pkg
        for pkg in target_packages
        if base_lookup.get((pkg["package_name"], pkg["type"])) != pkg["version"]
    ]


def _resolve_python_csv_licenses(
    packages: list[dict[str, str]], cache_path: str | None
) -> None:
    """Fill UNKNOWN ``spdx_license`` on python rows by querying PyPI in place.

    Mutates the rows so the existing CSV writer doesn't need to know about the
    lookup. Non-python rows and rows with an existing license are skipped, so
    this is safe to call unconditionally when ``--lookup-licenses`` is on.
    """
    mod = _lockfile_module("license_lookup")
    if mod is None or not hasattr(mod, "resolve_licenses"):
        return
    queries: list[dict[str, str]] = []
    indices: list[int] = []
    for i, pkg in enumerate(packages):
        if pkg.get("type") != "python":
            continue
        if (pkg.get("spdx_license") or "UNKNOWN") != "UNKNOWN":
            continue
        queries.append(
            {
                "name": pkg.get("package_name", ""),
                "version": pkg.get("version", ""),
                "ecosystem": "pypi",
            }
        )
        indices.append(i)
    if not queries:
        return
    resolved = mod.resolve_licenses(queries, cache_path=cache_path)
    for i, entry in zip(indices, resolved):
        packages[i]["spdx_license"] = entry.get("license") or "UNKNOWN"


@functools.lru_cache(maxsize=None)
def _load_overrides(yaml_path: str) -> dict[tuple[str, str], str]:
    """Load (ecosystem, name) -> SPDX license map from a hand-curated YAML.

    Used to fill in licenses for packages that public registries cannot
    resolve (NVIDIA-internal CUDA components, repackaged wheels with empty
    PyPI metadata, OpenSSL's DEP-5 copyright file, etc). Missing or unreadable
    YAML returns an empty map - the rest of the pipeline degrades to UNKNOWN
    just as it did before.
    """
    if yaml is None or not yaml_path:
        return {}
    path = Path(yaml_path)
    if not path.is_file():
        return {}
    try:
        data = yaml.safe_load(path.read_text(encoding="utf-8")) or {}
    except yaml.YAMLError as exc:  # pragma: no cover - defensive
        print(f"WARNING: failed to parse {yaml_path}: {exc}", file=sys.stderr)
        return {}
    out: dict[tuple[str, str], str] = {}
    for entry in data.get("overrides", []) or []:
        ecosystem = (entry.get("ecosystem") or "").strip().lower()
        name = (entry.get("name") or "").strip()
        license_id = (entry.get("license") or "").strip()
        if ecosystem and name and license_id:
            out[(ecosystem, name)] = license_id
    return out


def _apply_overrides_to_csv(
    packages: list[dict[str, str]], overrides_path: str | None
) -> int:
    """Replace UNKNOWN ``spdx_license`` rows whose (type, name) is in the overrides.

    Returns the count of rows that were rewritten so the caller can log it.
    No-ops when the overrides YAML is missing or empty.
    """
    if not overrides_path:
        return 0
    overrides = _load_overrides(overrides_path)
    if not overrides:
        return 0
    rewritten = 0
    for pkg in packages:
        if (pkg.get("spdx_license") or "UNKNOWN") != "UNKNOWN":
            continue
        key = (
            (pkg.get("type") or "").strip().lower(),
            (pkg.get("package_name") or "").strip(),
        )
        license_id = overrides.get(key)
        if license_id:
            pkg["spdx_license"] = license_id
            rewritten += 1
    return rewritten


def _apply_overrides_to_pkgs(
    pkgs: list[dict[str, Any]],
    ecosystem: str,
    overrides_path: str | None,
) -> int:
    """Replace UNKNOWN ``license`` entries in the markdown-writer schema.

    Mirrors ``_apply_overrides_to_csv`` but for the ``{name, version, license}``
    shape used by the ATTRIBUTIONS-*.md renderers.
    """
    if not overrides_path:
        return 0
    overrides = _load_overrides(overrides_path)
    if not overrides:
        return 0
    rewritten = 0
    for pkg in pkgs:
        if (pkg.get("license") or "UNKNOWN") != "UNKNOWN":
            continue
        key = (ecosystem, (pkg.get("name") or "").strip())
        license_id = overrides.get(key)
        if license_id:
            pkg["license"] = license_id
            rewritten += 1
    return rewritten


def _dedupe_csv_rows(packages: list[dict[str, str]]) -> list[dict[str, str]]:
    """Collapse duplicate (package_name, version, type) rows from the CSV input.

    Mirrors ``license_lookup.dedupe_packages`` but keys on the CSV schema
    (``package_name``/``type``) and includes ``type`` in the key so a package
    name appearing as both dpkg and python stays as two rows. Prefers the row
    with a non-UNKNOWN ``spdx_license``.
    """
    seen: dict[tuple[str, str, str], int] = {}
    out: list[dict[str, str]] = []
    for pkg in packages:
        key = (pkg.get("package_name", ""), pkg.get("version", ""), pkg.get("type", ""))
        if key not in seen:
            seen[key] = len(out)
            out.append(pkg)
            continue
        existing = out[seen[key]]
        if (existing.get("spdx_license") or "UNKNOWN") == "UNKNOWN" and (
            pkg.get("spdx_license") or "UNKNOWN"
        ) != "UNKNOWN":
            out[seen[key]] = pkg
    return out


def write_csv(packages: list[dict[str, str]], output_path: Path | None) -> None:
    """Write packages to CSV sorted by (type, package_name)."""
    sorted_packages = sorted(packages, key=lambda p: (p["type"], p["package_name"]))
    fieldnames = ["package_name", "version", "type", "spdx_license"]
    if output_path:
        with open(output_path, "w", newline="") as f:
            writer = csv.DictWriter(f, fieldnames=fieldnames)
            writer.writeheader()
            writer.writerows(sorted_packages)
        print(f"Wrote {len(sorted_packages)} entries to {output_path}", file=sys.stderr)
    else:
        writer = csv.DictWriter(sys.stdout, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(sorted_packages)


# ---- SPDX helpers -------------------------------------------------------------


def load_spdx(spdx_path: Path) -> dict[str, Any] | None:
    """Load an SPDX 2.3 JSON document, or return None if the file is absent."""
    if not spdx_path.is_file():
        return None
    try:
        return json.loads(spdx_path.read_text(encoding="utf-8"))
    except json.JSONDecodeError as exc:
        print(f"WARNING: {spdx_path} is not valid JSON: {exc}", file=sys.stderr)
        return None


def _spdx_license_for_pkg(pkg: dict[str, Any]) -> str:
    """Pick a license ID for an SPDX package. Falls back to UNKNOWN."""
    for key in ("licenseConcluded", "licenseDeclared"):
        val = pkg.get(key)
        if val and val not in ("NOASSERTION", "NONE", ""):
            return val
    return "UNKNOWN"


def _purl_prefix(purl: str) -> str:
    """Return the `pkg:<type>/` prefix (lowercase) of a purl, empty if none."""
    if not purl.startswith("pkg:"):
        return ""
    rest = purl[len("pkg:") :]
    slash = rest.find("/")
    if slash < 0:
        return ""
    return "pkg:" + rest[:slash].lower() + "/"


def _iter_spdx_packages_by_purl(
    spdx: dict[str, Any], purl_type: str
) -> list[dict[str, str]]:
    """Yield {name, version, license, purl} for SPDX packages matching a purl type."""
    wanted = f"pkg:{purl_type.lower()}/"
    results: list[dict[str, str]] = []
    for pkg in spdx.get("packages", []) or []:
        refs = pkg.get("externalRefs") or []
        purl = ""
        for ref in refs:
            if (
                ref.get("referenceCategory") == "PACKAGE-MANAGER"
                and ref.get("referenceType") == "purl"
            ):
                locator = ref.get("referenceLocator", "")
                if _purl_prefix(locator) == wanted:
                    purl = locator
                    break
        if not purl:
            continue
        name = pkg.get("name") or ""
        version = pkg.get("versionInfo") or ""
        license_id = _spdx_license_for_pkg(pkg)
        results.append(
            {"name": name, "version": version, "license": license_id, "purl": purl}
        )
    return results


# ---- First-party Python filter ------------------------------------------------


def _is_first_party_python(name: str) -> bool:
    lower = name.lower()
    return lower.startswith("ai-dynamo") or lower.startswith("dynamo-")


# ---- Markdown rendering -------------------------------------------------------


def _render_attributions_md(
    ecosystem: str,
    packages: list[dict[str, str]],
    note: str = "",
) -> str:
    """Render an ATTRIBUTIONS-<Ecosystem>.md body.

    Each package dict must have: name, version, license, and optionally `note`.
    Entries are sorted case-insensitively by `name`.
    """
    header = _MD_HEADER.format(ecosystem=ecosystem)
    parts = [header]
    if note:
        parts.append("\n" + note.rstrip() + "\n")

    sorted_pkgs = sorted(packages, key=lambda p: p["name"].lower())
    entry_blocks: list[str] = []
    for pkg in sorted_pkgs:
        name = pkg["name"]
        version = pkg.get("version") or ""
        license_id = pkg.get("license") or "UNKNOWN"
        subnote = pkg.get("note") or ""
        lines = [
            f"## {name} ({version})" if version else f"## {name}",
            f"**License:** `{license_id}`",
        ]
        if subnote:
            lines.append(subnote)
        entry_blocks.append("\n".join(lines))

    if entry_blocks:
        parts.append("\n" + "\n\n".join(entry_blocks) + "\n")

    text = "".join(parts)
    # Ensure exactly one trailing newline.
    return text.rstrip("\n") + "\n"


def _render_native_md(packages: list[dict[str, Any]]) -> str:
    """Render ATTRIBUTIONS-Native.md.

    Schema differs from the per-ecosystem renderer: each artifact becomes its own
    entry with `## name (license) — Source: <repo>`.
    """
    header = _MD_HEADER.format(ecosystem="Native")
    intro = (
        "\n"
        "These artifacts are built from source by Dynamo Dockerfiles and are not\n"
        "detected by Syft as owned packages. Metadata below is hand-curated in\n"
        "`container/compliance/native_packages.yaml`.\n"
    )
    entries: list[dict[str, str]] = []
    for pkg in packages:
        repo = pkg.get("repo", "")
        for artifact in pkg.get("artifacts", []) or []:
            entries.append(
                {
                    "name": artifact.get("name", ""),
                    "license": artifact.get("license", "UNKNOWN"),
                    "repo": repo,
                }
            )
    entries.sort(key=lambda e: e["name"].lower())
    blocks = [f"## {e['name']} ({e['license']}) — Source: {e['repo']}" for e in entries]
    parts = [header, intro]
    if blocks:
        parts.append("\n" + "\n\n".join(blocks) + "\n")
    text = "".join(parts)
    return text.rstrip("\n") + "\n"


# ---- Lockfile module loader (direct file import avoids needing a package) -----


def _load_module_from_path(module_name: str, file_path: Path):
    """Load a Python module directly from a filesystem path.

    The repository does not ship `container/__init__.py`, so
    `container.compliance.lockfile.*` imports would fail during CI in the
    compliance container image. Using importlib.util.spec_from_file_location keeps
    this script independent of package layout.
    """
    if not file_path.is_file():
        return None
    spec = importlib.util.spec_from_file_location(module_name, str(file_path))
    if spec is None or spec.loader is None:
        return None
    module = importlib.util.module_from_spec(spec)
    try:
        spec.loader.exec_module(module)  # type: ignore[union-attr]
    except Exception as exc:  # pragma: no cover - defensive
        print(
            f"WARNING: failed to import {file_path}: {exc}",
            file=sys.stderr,
        )
        return None
    return module


@functools.lru_cache(maxsize=None)
def _lockfile_module(name: str):
    """Load cargo.py / gomod.py / license_lookup.py from container/compliance/lockfile/.

    Cached so a single CLI invocation re-uses the same module instance across
    every writer call. This keeps license_lookup's per-host throttle state and
    SQLite connection pool consistent when both Rust and Go writers run.
    """
    here = Path(__file__).resolve().parent
    return _load_module_from_path(
        f"_compliance_lockfile_{name}", here / "lockfile" / f"{name}.py"
    )


# ---- Writers ------------------------------------------------------------------


def _resolve_licenses(
    entries: list[dict[str, Any]],
    cache_path: str | None,
) -> list[dict[str, Any]]:
    """Annotate lockfile entries via ``license_lookup.resolve_licenses``.

    Pass-through (with a stderr warning) if the lookup module fails to load,
    so the writers always produce an output even on a broken install.
    """
    mod = _lockfile_module("license_lookup")
    if mod is None or not hasattr(mod, "resolve_licenses"):
        print(
            "WARNING: license_lookup module not available; skipping license resolution",
            file=sys.stderr,
        )
        return entries
    return mod.resolve_licenses(entries, cache_path=cache_path)


def _dedupe_packages(packages: list[dict[str, Any]]) -> list[dict[str, Any]]:
    """Collapse duplicate (name, version) rows via ``license_lookup.dedupe_packages``."""
    mod = _lockfile_module("license_lookup")
    if mod is None or not hasattr(mod, "dedupe_packages"):
        return packages
    return mod.dedupe_packages(packages)


def write_attributions_apt(
    spdx: dict[str, Any] | None,
    out_path: Path,
    *,
    dedupe: bool = False,
    overrides_path: str | None = None,
) -> int:
    if spdx is None:
        return 0
    pkgs = _iter_spdx_packages_by_purl(spdx, "deb")
    if dedupe:
        pkgs = _dedupe_packages(pkgs)
    _apply_overrides_to_pkgs(pkgs, "dpkg", overrides_path)
    out_path.write_text(
        _render_attributions_md("Apt (Debian/Ubuntu)", pkgs), encoding="utf-8"
    )
    return len(pkgs)


def write_attributions_python(
    spdx: dict[str, Any] | None,
    out_path: Path,
    *,
    dedupe: bool = False,
    overrides_path: str | None = None,
) -> int:
    if spdx is None:
        return 0
    pkgs = [
        p
        for p in _iter_spdx_packages_by_purl(spdx, "pypi")
        if not _is_first_party_python(p["name"])
    ]
    if dedupe:
        pkgs = _dedupe_packages(pkgs)
    _apply_overrides_to_pkgs(pkgs, "python", overrides_path)
    out_path.write_text(
        _render_attributions_md("Python (PyPI)", pkgs), encoding="utf-8"
    )
    return len(pkgs)


def write_attributions_rust(
    cargo_lock_path: Path,
    out_path: Path,
    *,
    lookup_licenses: bool = False,
    license_cache_path: str | None = None,
    dedupe: bool = False,
) -> int:
    mod = _lockfile_module("cargo")
    if mod is None or not hasattr(mod, "parse_cargo_lock"):
        print(
            "WARNING: cargo lockfile module not available; skipping ATTRIBUTIONS-Rust.md",
            file=sys.stderr,
        )
        return 0
    content = cargo_lock_path.read_text(encoding="utf-8")
    workspace_members: list[str] | None = None
    if hasattr(mod, "get_workspace_members"):
        try:
            workspace_members = mod.get_workspace_members(
                str(cargo_lock_path.resolve().parent)
            )
        except Exception:
            workspace_members = None
    entries = mod.parse_cargo_lock(content, workspace_members)
    if lookup_licenses:
        entries = _resolve_licenses(entries, license_cache_path)
    pkgs = [
        {
            "name": e.get("name", ""),
            "version": e.get("version", ""),
            "license": e.get("license") or "UNKNOWN",
        }
        for e in entries
    ]
    if dedupe:
        pkgs = _dedupe_packages(pkgs)
    out_path.write_text(_render_attributions_md("Rust (Cargo)", pkgs), encoding="utf-8")
    return len(pkgs)


def write_attributions_go(
    go_mod_paths: list[Path],
    out_path: Path,
    *,
    lookup_licenses: bool = False,
    license_cache_path: str | None = None,
    dedupe: bool = False,
) -> int:
    mod = _lockfile_module("gomod")
    if mod is None or not hasattr(mod, "parse_go_mod"):
        print(
            "WARNING: gomod lockfile module not available; skipping ATTRIBUTIONS-Go.md",
            file=sys.stderr,
        )
        return 0
    merged: dict[tuple[str, str], dict[str, Any]] = {}
    for path in go_mod_paths:
        if not path.is_file():
            print(f"WARNING: go.mod not found: {path}", file=sys.stderr)
            continue
        content = path.read_text(encoding="utf-8")
        for entry in mod.parse_go_mod(content):
            key = (entry.get("name", ""), entry.get("version", ""))
            merged.setdefault(key, entry)
    entries = list(merged.values())
    if lookup_licenses:
        entries = _resolve_licenses(entries, license_cache_path)
    pkgs = [
        {
            "name": e.get("name", ""),
            "version": e.get("version", ""),
            "license": e.get("license") or "UNKNOWN",
        }
        for e in entries
    ]
    if dedupe:
        pkgs = _dedupe_packages(pkgs)
    out_path.write_text(_render_attributions_md("Go (go.mod)", pkgs), encoding="utf-8")
    return len(pkgs)


def write_attributions_native(yaml_path: Path, out_path: Path) -> int:
    if yaml is None:
        print(
            "WARNING: PyYAML not installed; skipping ATTRIBUTIONS-Native.md",
            file=sys.stderr,
        )
        return 0
    if not yaml_path.is_file():
        print(
            f"WARNING: native packages overlay not found: {yaml_path}; skipping ATTRIBUTIONS-Native.md",
            file=sys.stderr,
        )
        return 0
    data = yaml.safe_load(yaml_path.read_text(encoding="utf-8")) or {}
    packages = data.get("packages", []) or []
    out_path.write_text(_render_native_md(packages), encoding="utf-8")
    return sum(len(p.get("artifacts", []) or []) for p in packages)


# ---- CLI ----------------------------------------------------------------------


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Convert extraction output to attribution CSV and ATTRIBUTIONS-*.md files",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  %(prog)s --target-dir ./output --output attribution.csv
  %(prog)s --target-dir ./output --base-dir ./base-output --output attribution.csv
  %(prog)s --target-dir ./output --cargo-lock Cargo.lock --go-mod go.mod
        """,
    )
    parser.add_argument(
        "--target-dir",
        required=True,
        help="Directory containing dpkg.tsv, python.tsv, and (optionally) sbom.spdx.json",
    )
    parser.add_argument(
        "--base-dir",
        help="Directory containing base image extraction (enables diff output)",
    )
    parser.add_argument(
        "--output",
        "-o",
        help="Output CSV file path (default: stdout)",
    )
    parser.add_argument(
        "--attributions-dir",
        help="Directory to write ATTRIBUTIONS-*.md (default: --target-dir)",
    )
    parser.add_argument(
        "--cargo-lock",
        help="Path to a Cargo.lock to emit ATTRIBUTIONS-Rust.md (omitted = no Rust output)",
    )
    parser.add_argument(
        "--go-mod",
        action="append",
        default=[],
        help="Path to a go.mod to include in ATTRIBUTIONS-Go.md (repeatable)",
    )
    parser.add_argument(
        "--native-packages",
        default=str(Path(__file__).resolve().parent / "native_packages.yaml"),
        help="Path to native_packages.yaml overlay (default: alongside this script)",
    )
    parser.add_argument(
        "--lookup-licenses",
        action="store_true",
        help=(
            "Resolve UNKNOWN Rust/Go licenses by querying crates.io and "
            "deps.dev. Cached in --license-cache. Default: off."
        ),
    )
    parser.add_argument(
        "--license-cache",
        help=(
            "SQLite file used to cache license-lookup results "
            "(default: ~/.cache/dynamo-compliance/license-lookup.sqlite)."
        ),
    )
    parser.add_argument(
        "--dedupe",
        action="store_true",
        help=(
            "Collapse duplicate (name, version) rows that arise when syft "
            "sees the same package installed in two paths. Preserves the "
            "row with a non-UNKNOWN license."
        ),
    )
    parser.add_argument(
        "--license-overrides",
        default=str(Path(__file__).resolve().parent / "license_overrides.yaml"),
        help=(
            "Path to a hand-curated YAML mapping (ecosystem, name) -> SPDX "
            "license, applied to UNKNOWN rows after lookups. Use --license-overrides "
            "'' to disable. Default: license_overrides.yaml alongside this script."
        ),
    )
    args = parser.parse_args()
    overrides_path = args.license_overrides or None

    target_dir = Path(args.target_dir)
    if not target_dir.is_dir():
        print(f"ERROR: --target-dir does not exist: {target_dir}", file=sys.stderr)
        sys.exit(1)

    # --- CSV pipeline (byte-identical to prior behavior unless --dedupe) ---
    target_packages = read_extraction_dir(target_dir)
    if not target_packages:
        print(f"ERROR: no packages found in {target_dir}", file=sys.stderr)
        sys.exit(1)

    if args.lookup_licenses:
        _resolve_python_csv_licenses(target_packages, args.license_cache)

    if args.dedupe:
        before = len(target_packages)
        target_packages = _dedupe_csv_rows(target_packages)
        dropped = before - len(target_packages)
        if dropped:
            print(f"  dedupe: dropped {dropped} duplicate CSV rows", file=sys.stderr)

    overrides_count = _apply_overrides_to_csv(target_packages, overrides_path)
    if overrides_count:
        print(
            f"  overrides: rewrote {overrides_count} UNKNOWN rows from "
            f"{overrides_path}",
            file=sys.stderr,
        )

    output_path = Path(args.output) if args.output else None
    write_csv(target_packages, output_path)

    if args.base_dir:
        base_dir = Path(args.base_dir)
        if not base_dir.is_dir():
            print(f"ERROR: --base-dir does not exist: {base_dir}", file=sys.stderr)
            sys.exit(1)
        base_packages = read_extraction_dir(base_dir)
        diff_packages = compute_diff(target_packages, base_packages)
        print(
            f"Diff: {len(diff_packages)} new/changed packages vs base", file=sys.stderr
        )

        if output_path:
            diff_path = output_path.with_stem(output_path.stem + "_diff")
            write_csv(diff_packages, diff_path)
        else:
            print("\n# --- DIFF (new/changed packages vs base) ---", file=sys.stderr)
            write_csv(diff_packages, None)

    # --- ATTRIBUTIONS-*.md outputs (additive) ---
    attributions_dir = (
        Path(args.attributions_dir) if args.attributions_dir else target_dir
    )
    attributions_dir.mkdir(parents=True, exist_ok=True)

    spdx = load_spdx(target_dir / "sbom.spdx.json")

    apt_count = write_attributions_apt(
        spdx,
        attributions_dir / "ATTRIBUTIONS-Apt.md",
        dedupe=args.dedupe,
        overrides_path=overrides_path,
    )
    py_count = write_attributions_python(
        spdx,
        attributions_dir / "ATTRIBUTIONS-Python.md",
        dedupe=args.dedupe,
        overrides_path=overrides_path,
    )
    if spdx is None:
        print(
            f"NOTE: {target_dir / 'sbom.spdx.json'} not found; skipped Apt/Python attributions",
            file=sys.stderr,
        )
    else:
        print(
            f"Wrote ATTRIBUTIONS-Apt.md ({apt_count}) and ATTRIBUTIONS-Python.md ({py_count})",
            file=sys.stderr,
        )

    if args.cargo_lock:
        count = write_attributions_rust(
            Path(args.cargo_lock),
            attributions_dir / "ATTRIBUTIONS-Rust.md",
            lookup_licenses=args.lookup_licenses,
            license_cache_path=args.license_cache,
            dedupe=args.dedupe,
        )
        print(f"Wrote ATTRIBUTIONS-Rust.md ({count})", file=sys.stderr)

    if args.go_mod:
        count = write_attributions_go(
            [Path(p) for p in args.go_mod],
            attributions_dir / "ATTRIBUTIONS-Go.md",
            lookup_licenses=args.lookup_licenses,
            license_cache_path=args.license_cache,
            dedupe=args.dedupe,
        )
        print(f"Wrote ATTRIBUTIONS-Go.md ({count})", file=sys.stderr)

    native_count = write_attributions_native(
        Path(args.native_packages), attributions_dir / "ATTRIBUTIONS-Native.md"
    )
    if native_count:
        print(
            f"Wrote ATTRIBUTIONS-Native.md ({native_count} artifacts)",
            file=sys.stderr,
        )


if __name__ == "__main__":
    main()
