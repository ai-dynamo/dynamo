// ---------------------------------------------------------------------------
// InputItem — untagged enum: ItemReference | Item | EasyInputMessage
// ---------------------------------------------------------------------------

/// Input item that can be used in the context for generating a response.
#[derive(Debug, Serialize, Deserialize, Clone, PartialEq)]
#[serde(untagged)]
pub enum InputItem {
    /// Reference to an existing item by ID.
    ItemReference(ItemReference),
    /// Structured item with type discrimination.
    Item(Item),
    /// Simple user-friendly message.
    EasyMessage(EasyInputMessage),
}

// ---------------------------------------------------------------------------
// InputParam — top-level input to CreateResponse
// ---------------------------------------------------------------------------

/// The input to the model: either a text string or a list of items.
#[derive(Debug, Serialize, Deserialize, Clone, PartialEq)]
#[serde(untagged)]
pub enum InputParam {
    /// A text input to the model.
    Text(String),
    /// A list of input items.
    Items(Vec<InputItem>),
}

impl Default for InputParam {
    fn default() -> Self {
        InputParam::Text(String::new())
    }
}

