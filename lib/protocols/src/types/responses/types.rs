// ---------------------------------------------------------------------------
// OutputTextContent — the root cause: `annotations` needs `#[serde(default)]`
// ---------------------------------------------------------------------------

/// A text output from the model.
#[derive(Debug, Serialize, Deserialize, Clone, PartialEq)]
pub struct OutputTextContent {
    /// The annotations of the text output.
    #[serde(default)]
    pub annotations: Vec<Annotation>,
    /// Log probabilities for the output tokens, if requested.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub logprobs: Option<Vec<LogProb>>,
    /// The text output from the model.
    pub text: String,
}

// ---------------------------------------------------------------------------
// OutputMessageContent — references our OutputTextContent
// ---------------------------------------------------------------------------

/// Content types that can appear in an assistant output message.
#[derive(Debug, Serialize, Deserialize, Clone, PartialEq)]
#[serde(tag = "type", rename_all = "snake_case")]
pub enum OutputMessageContent {
    /// A text output from the model.
    OutputText(OutputTextContent),
    /// A refusal from the model.
    Refusal(RefusalContent),
}

// ---------------------------------------------------------------------------
// OutputContent — used in stream events (content_part.added / .done)
// ---------------------------------------------------------------------------

/// Content types emitted in streaming content-part events.
#[derive(Debug, Serialize, Deserialize, Clone, PartialEq)]
#[serde(tag = "type", rename_all = "snake_case")]
pub enum OutputContent {
    OutputText(OutputTextContent),
    Refusal(RefusalContent),
    ReasoningText(ReasoningTextContent),
}

// ---------------------------------------------------------------------------
// OutputMessage — references our OutputMessageContent
// ---------------------------------------------------------------------------

/// A message generated by the model.
#[derive(Debug, Serialize, Deserialize, Clone, PartialEq)]
pub struct OutputMessage {
    /// The content of the output message.
    pub content: Vec<OutputMessageContent>,
    /// The unique ID of the output message.
    pub id: String,
    /// The role of the output message. Always `assistant`.
    pub role: AssistantRole,
    /// Commentary vs final answer phase.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub phase: Option<MessagePhase>,
    /// The status of the message.
    pub status: OutputStatus,
}

// ---------------------------------------------------------------------------
// MessageItem — untagged enum discriminating Output vs Input
// ---------------------------------------------------------------------------

/// A message item: either an output (assistant) or input (user/system/developer).
#[derive(Debug, Serialize, Deserialize, Clone, PartialEq)]
#[serde(untagged)]
pub enum MessageItem {
    /// Output must come first — has required `id` and `status` fields.
    Output(OutputMessage),
    /// Input message (user/system/developer).
    Input(InputMessage),
}

