// We re-define the types in the deserialization chain from InputParam down to
// OutputTextContent so we can relax serde attributes (e.g. `#[serde(default)]`
// on `annotations`). All other response types are re-exported from upstream.
//
// This mirrors the pattern used for `CreateChatCompletionRequest` in
// `lib/protocols/src/types/chat.rs`.

