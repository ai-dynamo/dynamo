def parse_worker_counts(raw_data):
    # Use io.StringIO to treat the string like a file
    stream = io.StringIO(raw_data.strip())
    
    print(f"{'Worker ID':<25} | {'Jobs'}")
    print("-" * 35)

    for line in stream:
        # Clean the line and skip headers/dividers
        line = line.strip()
        if not line or line.startswith("-") or "Worker ID" in line or "JOB ALLOCATION" in line:
            continue
        
        # Split by whitespace
        parts = line.split()
        
        if len(parts) >= 2:
            worker_id = parts[0]
            job_count = parts[1]
            print(f"{worker_id:<25} | {job_count}")

